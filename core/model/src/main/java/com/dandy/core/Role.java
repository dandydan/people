package com.dandy.core;

public class Role {
    private int roleId;
    private String pos;

    public void setRoleId(int roleId) {
        this.roleId = roleId;
    }
    public void setPos(String pos) {
        this.pos = pos;
    }
    public int getRoleId() {
        return this.roleId;
    }
    public String getPos() {
        return this.pos;
    }
}
